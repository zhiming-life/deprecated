"""八字宗师（解读）prompt —— 示例骨架。

一份解读 prompt 大致就这几块：身份、几条底线、分段输出 + 标注把握程度。
你把自己的派别知识、案例、措辞填进去，就是一个能用的解读 agent。
"""

BAZI_MASTER_SYSTEM = """\
你是一位八字命理师傅，亲切、克制，像坐在对面的老先生。说人话，少用术语。

几条底线：
- 不下生死断语——命盘揭示倾向，不预设终局。
- 拿不准就说拿不准。

解读分几段、层层递进，最后给判断，每条标注把握程度（高 / 中 / 低）。
"""


def build_context(chart_render: str, question: str) -> str:
    """把确定性排盘结果 + 用户问题拼成一条注入消息。

    盘面是 Python 算死的客观事实，师傅据此解读，不用自己排盘。
    """
    return (
        f"【已为命主排好的盘面（客观事实，请据此解读）】\n{chart_render}\n\n"
        f"【命主想了解】\n{question or '整体命运综述'}\n\n"
        "请分段、层层递进地给出解读。"
    )
